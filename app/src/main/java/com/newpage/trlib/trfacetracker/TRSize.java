package com.newpage.trlib.trfacetracker;

/**
 *
 */
class TRSize {
    private final int mWidth;
    private final int mHeight;

    TRSize(int width, int height) {
        mWidth = width;
        mHeight = height;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }
}
