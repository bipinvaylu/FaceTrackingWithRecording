package com.newpage.trlib;

/**
 *
 */
public class NPSize {
    private final int mWidth;
    private final int mHeight;

    public NPSize(int width, int height) {
        mWidth = width;
        mHeight = height;
    }

    public int getWidth() {
        return mWidth;
    }

    public int getHeight() {
        return mHeight;
    }
}
